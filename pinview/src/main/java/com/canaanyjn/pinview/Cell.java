public class Cell {
    private float x,y;
    private float radius;
    private boolean isChooesd = false;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getRadius() {
        return radius;
    }

    public void setRadius(float radius) {
        this.radius = radius;
    }

    public boolean isChooesd() {
        return isChooesd;
    }

    public void setIsChooesd(boolean isChooesd) {
        this.isChooesd = isChooesd;
    }

}
